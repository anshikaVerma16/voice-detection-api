def generate_voices():
    for language, sentences in texts.items():
        lang_codes = {
            'english': 'en',
            'tamil': 'ta',
            'hindi': 'hi',
            'malayalam': 'ml',
            'telugu': 'te'
        }
        
        lang_code = lang_codes[language]
        os.makedirs(f'data/ai/{language}', exist_ok=True)
        for i, text in enumerate(sentences, 1):
            try:
                tts = gTTS(text=text, lang=lang_code, slow=False)
                filename = f'data/ai/{language}/ai_gtts_{i}.mp3'
                tts.save(filename)
                print(f"Generated: ai_gtts_{i}.mp3")
            except Exception as e:
                print(f"Error on {i}: {e}")

if __name__ == "__main__":
    generate_voices()
